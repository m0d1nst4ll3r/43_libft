#include "libft.h"

int	ft_atoi(const char *s)
{
	int		neg;
	int		res;
	size_t	i;

	i = 0;
	res = 0;
	neg = 1;
	while (s[i] == ' ' || (s[i] >= 9 && s[i] <= 13))
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			neg = -1;
		i++;
	}
	while (s[i] >= '0' && s[i] <= '9')
	{
		res = res * 10 + neg * (s[i] - '0');
		i++;
	}
	return (res);
}
